fun main(){
    //Int to double
    var a:Int=10

    var b:Double=a.toDouble()
    println("Double value (from Integer):$b")

    //str to int
    var c:String="4456"

    var d:Int=c.toInt()
    println("Int value (from string):$d")

    //str to double
    var e:String="555"

    var f:Double=e.toDouble()
    println("Double value (from string):$f")

}
